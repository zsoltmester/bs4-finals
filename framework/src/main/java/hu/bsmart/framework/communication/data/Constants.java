package hu.bsmart.framework.communication.data;

public class Constants {
	/**
	 * Integer value that is illegal in normal network messages
	 * so it can be used to indicate an unset value.
	 */
	public static final int UNSET_INTEGER = -1;

	/**
	 * Long value that is illegal in normal network messages
	 * so it can be used to indicate an unset value.
	 */
	public static final long UNSET_LONG = -1L;

	/**
	 * Float value that is illegal in normal network messages
	 * so it can be used to indicate an unset value.
	 */
	public static final float UNSET_FLOAT = Float.NaN;
}
